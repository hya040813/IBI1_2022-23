def affordable_or_not(value, salary):
    """give a recommendation if a house is affordable based on salary."""
    if value <= 5 * salary:
        return "Yes"
    else:
        return "No"

# give an example
house_value = 350000
annual_salary = 100000
purchase_recommendation = affordable_or_not(house_value, annual_salary)
print(purchase_recommendation)
